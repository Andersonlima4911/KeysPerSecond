package me.roan.kps;

/**
 * Enum specifying all the different
 * layout positions for the graph
 * @author Roan
 */
public enum GraphMode {
	/**
	 * Indicates that the graph is placed
	 * in the same window as the other tiles
	 */
	Inline,
	/**
	 * Indicates that the graph is placed
	 * in it's own window
	 */
	Detached;
}
